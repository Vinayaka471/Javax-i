class Integer{
	public static void main(String[] args)
	{
		int age=55;
		int salary=8569;
		int year=2007;
		int id=21;
		int user=7;
		
		System.out.println("Initinal Values;");
		System.out.println("Age: "+age);
		System.out.println("Salary: "+salary);
		System.out.println("Year: "+year);
		System.out.println("ID: "+id);
		System.out.println("User: "+user);
		
		age=75;
		salary=20009;
		year=2019;
		id=27;
		user=2;
		
		
		
		System.out.println("Re-Initinal Values;");
		System.out.println("Age: "+age);
		System.out.println("Salary: "+salary);
		System.out.println("Year: "+year);
		System.out.println("ID: "+id);
		System.out.println("User: "+user);
	}
}