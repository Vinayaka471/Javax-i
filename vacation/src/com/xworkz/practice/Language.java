package com.xworkz.practice;

public class Language {
    public static void main(String[] args) {
        String Language="Java";

        switch(Language){
            case "MySQL":
                System.out.println("It is MySQL");
                break;
            case "C":
                System.out.println("It is C");
                break;
            case "HTML":
                System.out.println("It is HTML");
                break;
            case "Python":
                System.out.println("It is Python");
                break;
            case "Java":
                System.out.println("It is Java");
                break;
            case "CSS":
                System.out.println("It is CSS");
                break;
        }
    }
}
